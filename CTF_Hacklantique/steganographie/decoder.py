binary_string = "01001000010000010000000001001011000000000100000101001010000001000000000000000000010101010000000001011010000000000000000001100100011001000001001000101000000000110011000100101010010111110000000000000000001000000000100100010011001101000000000001110010001000000111110111100011"

# Diviser la chaîne en octets (8 bits)
bytes_list = [binary_string[i:i+8] for i in range(0, len(binary_string), 8)]

# Convertir chaque octet en caractère ASCII
decoded_text = "".join(chr(int(byte, 2)) for byte in bytes_list)

